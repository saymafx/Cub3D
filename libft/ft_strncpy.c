#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, ssize_t len)
{
	ssize_t	i;

	i = 0;
	while (src[i] != 0 && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
